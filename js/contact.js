
function openNav(){
  document.getElementById("contact").style.visibility = "visible";

  document.getElementById('contact').classList.remove('contactfermer');
  document.getElementById('contact').classList.add('contactouvrir');
}


function closeNav(){
  document.getElementById('contact').classList.remove('contactouvrir');
  document.getElementById('contact').classList.add('contactfermer');

}

